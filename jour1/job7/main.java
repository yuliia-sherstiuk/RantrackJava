public class Main {
    public static void main(String[] args) {
        int sum = 0; // // Déclare une variable pour stocker la somme

        // //additionner les nombres de 1 à 100
        for (int i = 1; i <= 100; i++) {
            sum += i; // // Ajoute i à la somme
        }

        System.out.println("The sum of the first 100 integers is: " + sum); // // Affiche le résultat
    }
}
