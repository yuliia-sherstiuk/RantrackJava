public class Main {
    public static void main(String[] args) {
        char c = 'A';                  // caractere (Lettre)
        String s = "Bonjour";          // chaine de charactéres (texte)
        int i = 42;                    // nombre entier
        long l = 123456789L;           // grand nombre entier
        float f = 3.14f;               // nombre à virgule flottante
        boolean b = true;              // valeur logique(vrai/faux)

        System.out.println("char: " + c);
        System.out.println("string: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("boolean: " + b);

        int TOTO = (int) 3.817;
        System.out.println("TOTO: " + TOTO); //print 3
    }
}